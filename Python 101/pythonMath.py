#Minimum value

firstDegree = 12
secondDegree = 983
thirdDegree = -364
minDegree = min(firstDegree, secondDegree, thirdDegree)

print("The minimum Degree is: ", minDegree)

#Maximum value

maxDegree = max(firstDegree,secondDegree,thirdDegree)

print("The maximum Degree is: ", maxDegree)

#Absolute value

absDegree = abs(minDegree)

print("Absolute valye is: ", absDegree)

#Power

powerValue =pow(firstDegree, 5)

print("The result of the power operation is : ", powerValue)
